        <h1>Cookies : comment les utilisons-nous ?</h1>
        <br>
        <strong class="article"><u>Article 1 : Qu’est ce qu’un cookie ?</u> &nbsp;</strong>
        <br>
        <p>
            Un cookie est un fichier texte susceptible d’être déposé dans un terminal lors de la consultation d’un
            service en ligne avec un logiciel de navigation. Un fichier cookie permet notamment à son émetteur, pendant
            sa durée de validité, de reconnaître le terminal concerné à chaque fois que ce terminal accède à un contenu
            numérique comportant des cookies du même émetteur.
            En tout état de cause, les cookies déposés sur votre terminal de navigation avec votre accord sont détruits
            13 mois après leur dépôt sur votre terminal.
        </p>
