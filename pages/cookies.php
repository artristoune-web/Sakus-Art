            d’enregistrer ou de consulter les cookies nécessaires à leur fonctionnement et que vous auriez refusés ou
            supprimés. Tel serait le cas si vous tentiez d’accéder à nos contenus ou services qui nécessitent de vous
            identifier. Tel serait également le cas lorsque nous (ou nos prestataires) ne pourrions pas reconnaître, à
            des fins de compatibilité technique, le type de navigateur utilisé par votre terminal, ses paramètres de
            langue et d’affichage ou le pays depuis lequel votre terminal semble connecté à Internet.
        </p>
        <strong class="article"><u>Article 4 : Comment configurer votre logiciel de navigation ?</u> &nbsp;</strong>
        <br>
        <p>
            Pour la gestion des cookies et de vos choix, la configuration de chaque navigateur est différente. Elle est
            décrite dans le menu d’aide de votre navigateur, qui vous permettra de savoir de quelle manière modifier vos
            souhaits en matière de cookies. Vous trouverez ci-dessous des informations concernant les principaux
            navigateurs.</p>

        <ul>
            <li><strong>Microsoft Edge</strong></li>
            <ol>Dans Microsoft Edge, cliquez sur le bouton Outils, puis sur Options Internet.
                Sous l’onglet Général, sous Historique de navigation, cliquez sur Paramètres.
                Cliquez sur le bouton Afficher les fichiers.</ol>
            <li><strong>Firefox</strong></li>
            <ol>Allez dans l’onglet Outils du navigateur puis sélectionnez le menu Options
                Dans la fenêtre qui s’affiche, choisissez Vie privée et cliquez sur Affichez les cookies</ol>
            <li><strong>Safari</strong></li>
            <ol>Accédez aux Paramètres via le menu du navigateur (Safari > Préférences).
                Cliquez sur Confidentialité.</ol>
            <li><strong>Google Chrome</strong></li>
            <ol>Accédez aux Paramètres via le bouton à droite de la barre d’URL ou via le menu du navigateur (Chrome >
                Préférences).
                Sélectionnez Paramètres Avancées.
                Cliquez sur Paramètres du contenu puis sur Cookies.</ol>
        </ul>
