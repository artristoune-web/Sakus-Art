                notre Site (rubriques et contenus visités, parcours), nous permettant d’améliorer l’intérêt et
                l’ergonomie
                de nos services ;
            </li>
            <li>
                d’adapter la présentation de notre Site aux préférences d’affichage de votre terminal lors de vos
                visites
                sur notre Site, selon les matériels et les logiciels de visualisation ou de lecture que votre terminal
                comporte ;
                de mémoriser des informations relatives à des oeuvres que vous avez consultées sur notre Site (wishlist,
                contenu d’un panier de commande, etc.) ;
            </li>
            <li>
