        <strong class="article"><u>Article 3 : Comment pouvez-vous contrôler les cookies utilisés ?</u> &nbsp;</strong>
        <br>
        <p>
            Vous pouvez à tout moment configurer votre logiciel de navigation de manière à ce que des cookies soient
            enregistrés dans votre terminal ou, au contraire, qu’ils soient rejetés (soit systématiquement, soit selon
            leur émetteur). Vous pouvez également configurer votre logiciel de navigation de manière à ce que
            l’acceptation ou le refus des cookies vous soient proposés ponctuellement, avant qu’un cookie puisse être
            enregistré dans votre terminal.
        </p>
